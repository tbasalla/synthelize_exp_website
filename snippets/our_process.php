				<p>STEP 4</p>
					<h2>Populate Visualizations and Dashboards</h2>
		<p>Having the data in one place enables us to begin to build out the dashboards and visualizations that you need to be able to answer your key questions. Our key philosophy is that data visualizations need to be simple enough that at a glance you can get the answer to the question you want, but rich enough that you can drill down to find more data to answer the question in a fuller way. For example, do you want to know if a particular initiative is on schedule? A simple green/yellow/red indicator can help you to answer the question. If you want more data beyond that you should be able to click on the item to see the components of data that make up the determination of on track or not.</p>
		<p>During this phase we will consider the types of reporting systems that might best suit your needs. In some cases we may be able to go with an existing software package such as Tableau. In other cases we may suggest that our team of developers code the types of visualizations that you need by hand. Your dashboards are driven by the complexity you need in terms of data display.</p>
