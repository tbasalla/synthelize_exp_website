				<p>STEP 3</p>
				<h2>Implement Data Architectures</h2>
		<p>This is where we get our hands dirty. When we know where you want to go and what systems you already have in place we can begin to design an overall data architecture for your organization. Sometimes this might mean considering replacing some of the data systems that were identified in step two if those systems do not collect all the data that is needed or if integration is difficult.</p>
		<p>In some cases this might also mean taking some of the ad-hoc spreadsheets and databases of an organization and converting them to more full-featured, available systems.</p>
		<p>In many cases it makes sense to build a centralized repository for your data&#8212;a data warehouse or data mart that gets loaded regularly with your key data elements. The design of such a system and its key parameters are driven by your vision and end goal.</p>
		<p>Once we have a design for a data warehouse and have evaluated your systems, it's time to build the highways to bring data into your warehouse. This often involves creating batch jobs to run on a regular basis to access the underlying APIs of your systems, extract and verify data, and load it into the data warehouse. In many cases other calculations are run to summarize or reconfigure data to be in a format that best supports your reporting requirements.</p>
