				<p>STEP 2</p>
				<h2>Evaluate Existing Data Systems</h2>
		<p>Once we have a vision for where you want to go in terms of data you can take a look at where you are currently at. This involves looking at your existing data systems and determining their capabilities. In many cases we will be able to adapt your existing data systems to send key information to a centralized data warehouse that can inform your decision making. In some other cases we may work with you to identify other systems to use that might better suit your needs.</p>
			<p>Part of this process is also identifying the "other places" where your organization is storing data. A surprising amount of data in any organization is stored in informal, ad-hoc spreadsheets and databases. Part of developing a data strategy is identifying those data stores and determining a way to make them more robust and to share the data with other parts of the company.</p>
